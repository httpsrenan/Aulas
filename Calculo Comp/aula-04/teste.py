print('\n')

# Lista ou vetor: É mutável, É ordenado, É indexado de 0..n
lista = [10,20,30]
# Tupla: Não é mutável, É ordenado, É indexado de 0..n
tupla = (10,20,30)
# dicionario: É mutável, É ordenado, É indexado pela chave
dicionario = {"num01" : 10, "num02" : 20, "num03" : 30,}
# matiz: vetor de vetor, ou vetor com mais de uma dimensão
matriz = [[10],[20],[30]]

print(type(lista))
print(type(tupla))
print(type(dicionario))
print(type(matriz))